	public static int initialX = 35, initialY = 25, pixelSize = 15;

	public static void main(String[] args) {
		JFrame frame = new JFrame("PIZZA");
		frame.setSize(800, 800);
		frame.setVisible(true);
		frame.setResizable(false);
		pixelArt art = new pixelArt();
		frame.add(art);
	}

	//Prints out the pixel art
	public void paint(Graphics g) {
		int currentX = initialX;
		int currentY = initialY;
		for (int i = 0; i < 48; i++) {
			for (int f = 0; f < 48; f++) {
				g.setColor(Colors[ColorGrid[i][f]]);
				g.fillRect(currentX, currentY, pixelSize, pixelSize);
				currentX += pixelSize;
			}
			currentY += pixelSize;
			currentX = initialX;
		}
	}
